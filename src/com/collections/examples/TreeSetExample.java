package com.collections.examples;

import java.util.TreeSet;

public class TreeSetExample {

	public static void main(String[] args) {
		TreeSet<Integer> ts = new TreeSet<Integer>();
		ts.add(23);
		ts.add(21);
		ts.add(29);
		ts.add(12);
		ts.add(4);
		ts.add(56);
		ts.add(43);
		ts.add(98);
		System.out.println(ts);
	}

}
